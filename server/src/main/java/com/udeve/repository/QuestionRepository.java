import com.udeve.entity.Question;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.domain.Specification;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.List;

@Repository
public interface QuestionRepository extends JpaRepository<Question, Integer> {

    Page<Question> findAll(Specification<Question> specification, Pageable pageable);

    void deleteByUserIdAndId(Integer userId,Integer id);

    Question findByUserIdAndId(Integer userId,Integer id);

    List<Question> findByTargetIdAndTargetTypeAndIsPublicIsTrue(Integer targetId,String targetType);

}
